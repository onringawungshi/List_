# Convert Character Matrix to single String;
# The original list is: [ ['g', 'f', 'g'], ['i', 's'], ['b', 'e', 's', 't'] ]
# The String after join: gfgisbest

lists=[['g','f','g'],['i','s'],['b','e','s','t']]
i=0
while i<len(lists):
    j=0
    while j<len(lists[i]):
        print(lists[i]+lists[j])
        j+=1
    i+=1
        












